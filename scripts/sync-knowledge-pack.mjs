import fs from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { buildKnowledgeArtifacts } from './knowledge-sync-shared.mjs'

const DEFAULT_SOURCE = '/Users/brandenschulze/Documents/Claude/Projects/T-Mobile Toolkit/customerconnect-knowledge-pack/exports/tmobile-knowledge.json'
const DEFAULT_IMAGES = '/Users/brandenschulze/Documents/Claude/Projects/T-Mobile Toolkit/customerconnect-knowledge-pack/exports/app-image-manifest.json'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const repoRoot = path.resolve(__dirname, '..')
const coreOutputPath = path.join(repoRoot, 'src/data/generated/tmobileKnowledgeCore.generated.ts')
const metaOutputPath = path.join(repoRoot, 'src/data/generated/tmobileKnowledgeMeta.generated.ts')
const rawOutputPath = path.join(repoRoot, 'public/knowledge/tmobile-knowledge-raw.json')
const overridesPath = path.join(repoRoot, 'config/knowledge-overrides.json')

function readArg(flag, fallback) {
  const match = process.argv.slice(2).find((arg) => arg.startsWith(`${flag}=`))
  return match ? match.slice(flag.length + 1) : fallback
}

async function readJson(filePath, label) {
  let contents

  try {
    contents = await fs.readFile(filePath, 'utf8')
  } catch (error) {
    throw new Error(`${label} file could not be read: ${filePath}\n${error instanceof Error ? error.message : String(error)}`)
  }

  try {
    return JSON.parse(contents)
  } catch (error) {
    throw new Error(`${label} file is not valid JSON: ${filePath}\n${error instanceof Error ? error.message : String(error)}`)
  }
}

function serialize(value) {
  return JSON.stringify(value, null, 2)
}

function serializeParsed(value, typeName) {
  return `JSON.parse(${JSON.stringify(serialize(value))}) as ${typeName}`
}

async function writeGeneratedTypeScript(filePath, contents) {
  await fs.mkdir(path.dirname(filePath), { recursive: true })
  await fs.writeFile(filePath, contents, 'utf8')
}

async function main() {
  const sourcePath = path.resolve(readArg('--source', DEFAULT_SOURCE))
  const imageManifestPath = path.resolve(readArg('--images', DEFAULT_IMAGES))

  const knowledgePack = await readJson(sourcePath, 'Knowledge pack')
  const imageManifest = await readJson(imageManifestPath, 'Image manifest')
  const overrides = await readJson(overridesPath, 'Knowledge overrides')

  const artifacts = buildKnowledgeArtifacts({
    knowledgePack,
    imageManifest,
    overrides,
    sourcePath,
    imageManifestPath,
  })

  const coreFileContents = `/* eslint-disable */
// This file is auto-generated by scripts/sync-knowledge-pack.mjs
// Do not edit by hand. Run \`npm run knowledge:sync\` after refreshing the external knowledge pack.

import type {
  KnowledgeCoreRecord,
  KnowledgeMatchOverrides,
  KnowledgePromo,
} from '../../types/knowledge'

export const TMOBILE_KNOWLEDGE_CORE_RECORDS = ${serializeParsed(artifacts.coreRecords, 'KnowledgeCoreRecord[]')}

export const TMOBILE_KNOWLEDGE_CORE_INDEX: Record<string, KnowledgeCoreRecord> = Object.fromEntries(
  TMOBILE_KNOWLEDGE_CORE_RECORDS.map((record) => [record.itemId, record])
)

export const TMOBILE_KNOWLEDGE_PROMOS = ${serializeParsed(artifacts.promos, 'KnowledgePromo[]')}

export const TMOBILE_KNOWLEDGE_MATCH_OVERRIDES = ${serializeParsed(artifacts.overrides, 'KnowledgeMatchOverrides')}
`

  const metaFileContents = `/* eslint-disable */
// This file is auto-generated by scripts/sync-knowledge-pack.mjs
// Do not edit by hand. Run \`npm run knowledge:sync\` after refreshing the external knowledge pack.

import type { KnowledgeMeta } from '../../types/knowledge'

export const TMOBILE_KNOWLEDGE_META = ${serializeParsed(artifacts.meta, 'KnowledgeMeta')}
`

  await writeGeneratedTypeScript(coreOutputPath, coreFileContents)
  await writeGeneratedTypeScript(metaOutputPath, metaFileContents)
  await fs.mkdir(path.dirname(rawOutputPath), { recursive: true })
  await fs.writeFile(rawOutputPath, `${serialize(artifacts.rawRecords)}\n`, 'utf8')

  console.log(`Knowledge core snapshot written to ${coreOutputPath}`)
  console.log(`Knowledge meta snapshot written to ${metaOutputPath}`)
  console.log(`Knowledge raw index written to ${rawOutputPath}`)
  console.log(`Imported ${artifacts.coreRecords.length} core records, ${artifacts.rawRecords.length} raw records, and ${artifacts.promos.length} promos`)
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error))
  process.exitCode = 1
})
